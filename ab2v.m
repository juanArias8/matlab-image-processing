function [m, a] = ab2v(v1, v2)
%--------------------------------------------------------------------------
%-- 1. Inicio de la función ab2v ------------------------------------------
%--------------------------------------------------------------------------
%--------------------------------------------------------------------------
%-- 2. Magnitud del vector es su normal -----------------------------------
%--------------------------------------------------------------------------
m(1) = norm(v1);%obtenemos la normal del vec1
m(2) = norm(v2);%obtenemos la normal del vec2
%--------------------------------------------------------------------------
%-- 3. minimos angulos de los vectores ------------------------------------
%--------------------------------------------------------------------------
a(1 : 3) = acosd(v1 / m(1));% Min. ángulos de vector 1 a ejes
a(4 : 6) = acosd(v2 / m(2)); % Min. ángulos de vector  a ejes
a(7) = acosd(dot(v1, v2) / m(1) / m(2)); %angulo entre los angulos
end

